
// 1.Check whether a number is positive or negative.

import java.util.*;
public class Main
{
	public static void main(String[] args) {
	    Scanner s=new Scanner(System.in);
	    int n=s.nextInt();
	    if(n>0){
		System.out.println("positive");
	    }
	    else{
	      System.out.println("negative");  
	    }
	}
}